package j0113;

public class Ja0113_04 {

	public static void main(String[] args) {
		// 문자 - g 저장해서 출력
		// 문자열 - "안녕하세요." 출력
		// 숫자 - 10000 입력해서 출력
		char ch = 'g';
		System.out.println(ch);
		
		String str = "안녕하세요.";
		System.out.println(str);
		
		int num = 10000;
		System.out.println(num);

		// int 7up = 10; // 숫자로 시작할 수 없음
		int up7 = 10;
		int a_ = 10;
		System.out.println(up7);
		System.out.println(a_);
		
		int last_index_of = 10;
		int lastIndexOf = 10;
		System.out.println(last_index_of);
		System.out.println(lastIndexOf);
		
		int pi = 3;
		final int PI = 3; // 상수 - 값을 변경 할 수 없음
		
		pi = 10;
		// PI = 10; // final 변수는 수정할 수 없음
		System.out.println(pi);
		System.out.println(PI);
		
		
		
	}

}
